const conn = process.env.DATABASE_URL;
if (!conn) {
  console.error("DATABASE_URL not set (run with: node --env-file=.env scripts/seed-special-ed-codes.mjs)");
  process.exit(1);
}

const client = new pg.Client({ connectionString: conn });
await client.connect();

async function seed(table, rows) {
  let n = 0;
  for (const [code, label] of rows) {
    await client.query(
      `INSERT INTO "${table}" (id, code, label, active)
       VALUES (gen_random_uuid()::text, $1, $2, true)
       ON CONFLICT (code) DO UPDATE SET label = EXCLUDED.label`,
      [code, label],
    );
    n++;
  }
  console.log(`  ${table}: upserted ${n} rows`);
}

console.log("Seeding special-ed code lookups…");
await seed("SpecialEdProblemCode", PROBLEM_CODES);
await seed("SpecialEdAccommodation", ACCOMMODATIONS);
await client.end();
console.log("Done.");
